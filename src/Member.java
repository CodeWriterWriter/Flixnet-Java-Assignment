
public class Member {
	private String name;
	private String account;
	
	public Member()
	{
		
	}
	
	public String getName()
	{
		return name;
	}
	
	public String getAccount()
	{
		return account;
	}
	
	public void setName(String newName)
	{
		name = newName;
	}
	
	public void setAccount(String newAccount)
	{
		account = newAccount;
	}
	

}